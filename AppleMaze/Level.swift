    class func tile(atRow row: Int, column: Int) -> Int {
    Level.maze[row * Level.height + column]
    }
    
    enum TileType: Int {
        case open = 0
        case wall = 1
        case portal = 2
        case start = 3
    }
    
    let width: Int = Level.width
    let height: Int = Level.height
    let pathfindingGraph: GKGridGraph<GKGridGraphNode>
    let startPosition: GKGridGraphNode
    let enemyStartPositions: [GKGridGraphNode]
    
    init() {
        let graph = GKGridGraph(fromGridStartingAt: vector_int2(0, 0), width: Int32(Level.width), height: Int32(Level.height), diagonalsAllowed: false)
        var walls = [GKGridGraphNode]()
        walls.reserveCapacity(Level.width * Level.height)
        var spawnPoints = [GKGridGraphNode]()
        var tempStartPosition: GKGridGraphNode!
        for w in 0 ..< Level.width {
            for h in 0 ..< Level.height {
                let tile = Level.tile(atRow: w, column: h)
                if tile == TileType.wall.rawValue {
                    walls.append(graph.node(atGridPosition: vector_int2(Int32(w), Int32(h)))!)
                } else if tile == TileType.portal.rawValue {
                    spawnPoints.append(graph.node(atGridPosition: vector_int2(Int32(w), Int32(h)))!)
                } else if tile == TileType.start.rawValue {
                    tempStartPosition = graph.node(atGridPosition: vector_int2(Int32(w), Int32(h)))!
                }
            }
        }
        startPosition = tempStartPosition
        graph.remove(walls)
        enemyStartPositions = spawnPoints
        pathfindingGraph = graph
    }
